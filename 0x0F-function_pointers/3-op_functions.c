#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - add 2 nums
 * @a: num 1
 * @b: num 2
 * Return: sum of a & b
 */
int op_add(int a, int b) {return (a + b); }
/**
 * op_sub - subtract nums
 * @a: num 1
 * @b: num 2
 * Return: a - b
 */
int op_sub(int a, int b) {return (a - b); }
/**
 * op_mul - multiply nums
 * @a: num 1
 * @b: num 2
 * Return: a * b
 */
int op_mul(int a, int b) {return (a * b); }
/**
 * op_div - divide nums
 * @a: num 1
 * @b: num 2
 * Return: a / b
 */
int op_div(int a, int b) {return (a / b); }
/**
 * op_mod - get reminder of nums
 * @a: num 1
 * @b: num 2
 * Return: a mod b
 */
int op_mod(int a, int b) {return (a % b); }
